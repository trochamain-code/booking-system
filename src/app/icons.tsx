// Shared stroke-icon set. Plain SVG functions (no "use client") so both server
// and client components can import them. Style matches the original dashboard
// nav icons: 24px viewBox, currentColor stroke, round caps/joins.
type IconProps = { className?: string; strokeWidth?: number };

function Svg({
  className = "h-5 w-5",
  strokeWidth = 2,
  children,
}: IconProps & { children: React.ReactNode }) {
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden
    >
      {children}
    </svg>
  );
}

